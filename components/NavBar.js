export default function NavBar() {
  const { theme } = useThemeContext();
  const router = useRouter();

  // check if currently on /define
  const ifDefine = router.pathname.substring(1) === 'define';

  return (
    <>
      <nav>
        {/* return normal navbar if not on /define */}
        {!ifDefine && (
          <ul>
            {routes.map((route) => {
              return <NavBarItem route={route} svg={svgs[route]} key={route} />;
            })}
          </ul>
        )}
        {/* return different navbar if on /define */}
        {ifDefine && (
          <ul>
            <NavBarItem route="Back" href="/search" svg={svgs['back']} />
            <BookmarkCheckBox />
          </ul>
        )}
      </nav>
      <style jsx>{`
        nav {
          /* fix to bottom of screen */
          width: 100%;
          position: fixed;
          bottom: 0;
        }

        ul {
          display: grid;
          grid-auto-flow: column;
          align-items: center;
        }
      `}</style>
      {/* separation of dynamic and static styles */}
      <style jsx>{`
        nav {
          background-color: ${theme.gray[2]};
        }
      `}</style>
    </>
  );
}
